"""
Вариативные вопросы для сбора дополнительного контекста.
"""
import random
from typing import List, Dict


def get_random_question() -> str:
    """Возвращает случайный вопрос для сбора контекста."""
    questions = [
        "Почему выбрали такие обороты? (станок ограничен / опыт подсказывает)",
        "Какой у вас станок? (CNC / универсальный)",
        "Какой инструмент используете?",
        "Была ли вибрация при таких оборотах?",
        "Какой получился класс шероховатости?",
    ]

    return random.choice(questions)


def get_material_prompt(material: str) -> str:
    """Получить подсказку по материалу."""
    prompts = {
        "титан": "Титан требует низких скоростей резания и хорошего охлаждения.",
        "алюминий": "Алюминий позволяет высокие скорости резания.",
        "нержавейка": "Нержавеющая сталь требует осторожного подбора режимов.",
        "чугун": "Чугун обрабатывается без СОЖ.",
    }

    return prompts.get(material, "")


def get_operation_prompt(operation: str) -> str:
    """Получить подсказку по операции."""
    prompts = {
        "токарка": "При токарной обработке основная скорость - скорость резания Vc.",
        "фрезерование": "Для фрезерования важен также осевой и радиальный шаг.",
        "сверление": "Для сверления критична подача на оборот.",
    }

    return prompts.get(operation, "")